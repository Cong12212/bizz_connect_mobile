  List<Widget> _buildDataSummary() {
    final merged = _mergeData();
    final items = <Widget>[];

    void addItem(String label, String? value) {
      if (value != null && value.trim().isNotEmpty) {
        items.add(
          Padding(
            padding: const EdgeInsets.only(bottom: 8),
            child: Row(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                SizedBox(
                  width: 100,
                  child: Text(
                    label,
                    style: const TextStyle(
                      fontSize: 12,
                      color: Color(0xFF64748B),
                      fontWeight: FontWeight.w500,
                    ),
                  ),
                ),
                Expanded(
                  child: Text(
                    value.trim(),
                    style: const TextStyle(fontSize: 14),
                  ),
                ),
              ],
            ),
          ),
        );
      }
    }

    addItem('Name', merged['name']);
    addItem('Email', merged['email']);
    addItem('Phone', merged['phone']);
    addItem('Company', merged['company']);
    addItem('Job Title', merged['jobTitle'] ?? merged['job_title']);
    addItem('Address', merged['addressDetail'] ?? merged['address_detail']);

    if (items.isEmpty) {
      return const [
        Text(
          'No data extracted. You can manually enter information in the next step.',
          style: TextStyle(
            fontSize: 12,
            color: Color(0xFF64748B),
            fontStyle: FontStyle.italic,
          ),
        ),
      ];
    }

    return items;
  }
