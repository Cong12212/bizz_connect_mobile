enum ScanStep { front, back, preview }

class ScanPage extends StatefulWidget {
  const ScanPage({super.key});

  @override
  State<ScanPage> createState() => _ScanPageState();
}

class _ScanPageState extends State<ScanPage> {
  ScanStep _step = ScanStep.front;
  final _ocrService = OcrService();
  final _imagePicker = ImagePicker();

  bool _isProcessing = false;
  File? _frontImage;
  File? _backImage;
  Map<String, String?>? _frontData;
  Map<String, String?>? _backData;

  @override
  void dispose() {
    _ocrService.dispose();
    super.dispose();
  }

  // ==== CHU·∫®N HO√Å KEY TR·∫¢ V·ªÄ T·ª™ OCR ====

  // Add method to handle QR scan
  Future<void> _scanQRCode() async {
    try {
      final result = await Navigator.push<String>(
        context,
        MaterialPageRoute(
          builder: (_) => const CameraScannerPage(isQrMode: true),
        ),
      );

      if (result == null || !mounted) return;

      debugPrint('üì± QR Code scanned: $result');

      setState(() => _isProcessing = true);

      try {
        // Try to parse as JSON
        final jsonData = jsonDecode(result) as Map<String, dynamic>;

        // Convert to Map<String, String?>
        final parsedData = <String, String?>{};
        jsonData.forEach((key, value) {
          parsedData[key] = value?.toString();
        });

        debugPrint('‚úÖ Parsed QR data: $parsedData');

        // Directly show preview with parsed data
        final contact = await showDialog<Contact?>(
          context: context,
          barrierDismissible: false,
          builder: (_) => ScannedContactPreviewDialog(scannedData: parsedData),
        );

        if (contact != null && mounted) {
          context.go('/contacts');
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('Contact "${contact.name}" added successfully'),
              backgroundColor: Colors.green,
            ),
          );
        }
      } on FormatException catch (e) {
        debugPrint('‚ö†Ô∏è QR data is not JSON, treating as plain text: $e');
        // If not JSON, treat as plain text (e.g., vCard)
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
              content: Text(
                'QR code format not supported. Please use business cards generated by this app.',
              ),
              backgroundColor: Colors.orange,
            ),
          );
        }
      }
    } catch (e) {
      debugPrint('‚ùå Error scanning QR code: $e');
      if (mounted) {
        ScaffoldMessenger.of(
          context,
        ).showSnackBar(SnackBar(content: Text('Error: $e')));
      }
    } finally {
      if (mounted) setState(() => _isProcessing = false);
    }
  }

  Future<void> _pickImage(ImageSource source) async {
    try {
      debugPrint('üì∏ Starting image picker for ${_step.name}...');

      final XFile? image = await _imagePicker.pickImage(
        source: source,
        imageQuality: 85,
      );

      if (image == null) {
        debugPrint('‚ùå No image selected');
        return;
      }

      debugPrint('‚úÖ Image selected: ${image.path}');
      setState(() => _isProcessing = true);

      final imageFile = File(image.path);

      // Try to detect and decode QR code from image first
      Map<String, String?>? qrData = await _tryDecodeQRFromImage(imageFile);

      if (qrData != null) {
        debugPrint('‚úÖ QR code detected in image, using QR data');

        // If QR code found, use it directly and skip to preview
        final contact = await showDialog<Contact?>(
          context: context,
          barrierDismissible: false,
          builder: (_) => ScannedContactPreviewDialog(scannedData: qrData),
        );

        if (contact != null && mounted) {
          context.go('/contacts');
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('Contact "${contact.name}" added from QR code'),
              backgroundColor: Colors.green,
            ),
          );
        }
        return;
      }

      // If no QR code found, fallback to OCR
      debugPrint('‚ÑπÔ∏è No QR code detected, using OCR');
      final result = await _ocrService.extractContactFromImage(imageFile);
      debugPrint('üìã OCR Result: $result');

      if (_step == ScanStep.front) {
        setState(() {
          _frontImage = imageFile;
          _frontData = result;
          _step = ScanStep.back;
        });

        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
              content: Text('Front side scanned. Now scan the back side.'),
              backgroundColor: Colors.green,
            ),
          );
        }
      } else if (_step == ScanStep.back) {
        setState(() {
          _backImage = imageFile;
          _backData = result;
          _step = ScanStep.preview;
        });
      }
    } catch (e) {
      debugPrint('‚ùå Error in _pickImage: $e');
      if (mounted) {
        ScaffoldMessenger.of(
          context,
        ).showSnackBar(SnackBar(content: Text('Error: $e')));
      }
    } finally {
      if (mounted) setState(() => _isProcessing = false);
    }
  }

  /// Try to decode QR code from image file
  /// Returns parsed data if QR found, null otherwise
  Future<Map<String, String?>?> _tryDecodeQRFromImage(File imageFile) async {
    try {
      debugPrint('üîç Attempting to decode QR code from image...');

      final controller = MobileScannerController(
        detectionSpeed: DetectionSpeed.noDuplicates,
        facing: CameraFacing.back,
        torchEnabled: false,
      );

      await controller.start();

      final bytes = await imageFile.readAsBytes();
      final barcode = await controller.analyzeImage(imageFile.path);

      await controller.dispose();

      if (barcode == null || barcode.barcodes.isEmpty) {
        debugPrint('‚ÑπÔ∏è No QR code found in image');
        return null;
      }

      for (final code in barcode.barcodes) {
        if (code.rawValue != null) {
          debugPrint('‚úÖ QR Code found: ${code.rawValue}');

          try {
            // Try to parse as JSON
            final jsonData = jsonDecode(code.rawValue!) as Map<String, dynamic>;

            // Convert to Map<String, String?>
            final parsedData = <String, String?>{};
            jsonData.forEach((key, value) {
              parsedData[key] = value?.toString();
            });

            debugPrint('‚úÖ QR data parsed successfully: $parsedData');
            return parsedData;
          } catch (e) {
            debugPrint('‚ö†Ô∏è QR data is not valid JSON: $e');
            continue;
          }
        }
      }

      return null;
    } catch (e) {
      debugPrint('‚ùå Error decoding QR from image: $e');
      return null;
    }
  }

  void _skipBackSide() {
    setState(() {
      _backImage = null;
      _backData = null;
      _step = ScanStep.preview;
    });
  }

  void _resetScan() {
    setState(() {
      _step = ScanStep.front;
      _frontImage = null;
      _backImage = null;
      _frontData = null;
      _backData = null;
    });
  }

  Map<String, String?> _mergeData() {
    final merged = <String, String?>{...(_frontData ?? {})};

    // Merge back data, ∆∞u ti√™n gi√° tr·ªã non-empty
    if (_backData != null) {
      _backData!.forEach((key, value) {
        if (value != null && value.trim().isNotEmpty) {
          if (merged[key] == null || merged[key]!.trim().isEmpty) {
            merged[key] = value;
          }
        }
      });
    }

    return merged;
  }

  Future<void> _showPreviewAndSave() async {
    final mergedData = _mergeData();

    final result = await showDialog<Contact?>(
      context: context,
      barrierDismissible: false,
      builder: (_) => ScannedContactPreviewDialog(scannedData: mergedData),
    );

    if (result != null && mounted) {
      debugPrint('‚úÖ Contact saved, navigating back...');
      context.go('/contacts');

      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Contact "${result.name}" added successfully'),
          backgroundColor: Colors.green,
        ),
      );
    }
  }

